function getAnswer() {
  const questionInput = document.getElementById("question");
  const answerBox = document.getElementById("answer");

  let q = questionInput.value.toLowerCase().trim();

  if (q === "") {
    answerBox.innerText = "Կարող ես գրել քո հարցը, ես կփորձեմ օգնել 🙂";
    return;
  }

  let response = "";

  // 🧮 Մաթեմատիկա
  if (q.includes("տոկոս") || q.includes("%")) {
    response =
      "Լավ հարց ես տվել 👍\n\n" +
      "Տոկոսը նշանակում է «հարյուրից մի մաս»։ Օրինակ՝ եթե ունենք 100 դրամ, " +
      "իսկ պետք է գտնել դրա 20%-ը, ապա կստանանք 20 դրամ։\n\n" +
      "Սովորաբար տոկոս հաշվում են այսպես՝ թիվ × տոկոս / 100։\n\n" +
      "Եթե ուզում ես, կարող ես գրել կոնկրետ օրինակ, ու միասին կլուծենք։";
  }

  // 📘 Հայոց լեզու
  else if (q.includes("գոյական") || q.includes("բայ")) {
    response =
      "Եկ սա պարզ բացատրեմ 🙂\n\n" +
      "Գոյականը բառ է, որը ցույց է տալիս անձ, առարկա կամ երևույթ։ " +
      "Օրինակ՝ գիրք, աշակերտ, հայրենիք։\n\n" +
      "Բայը ցույց է տալիս գործողություն՝ ինչ է անում։ Օրինակ՝ կարդում է, գրում է։\n\n" +
      "Եթե ուզում ես, կարող ենք միասին տարբերակել բառերը նախադասության մեջ։";
  }

  // 📜 Պատմություն
  else if (q.includes("պատերազմ") || q.includes("թագավոր")) {
    response =
      "Պատմական թեմաները սովորաբար ավելի հեշտ են հիշվում, " +
      "երբ հասկանում ենք պատճառը և հետևանքը։\n\n" +
      "Օրինակ՝ պատերազմները չեն սկսվում պատահական։ Միշտ կա պատճառ՝ " +
      "տարածք, իշխանություն կամ պաշտպանություն։\n\n" +
      "Ո՞ր ժամանակաշրջանի մասին է քո հարցը, կուզես ճշտել։";
  }

  // 💻 Ինֆորմատիկա
  else if (q.includes("ալգորիթմ") || q.includes("ծրագիր")) {
    response =
      "Շատ լավ թեմա ես ընտրել 👍\n\n" +
      "Ալգորիթմը քայլերի հստակ հաջորդականություն է, " +
      "որը օգնում է լուծել խնդիր։\n\n" +
      "Օրինակ՝ թեյ պատրաստելը նույնպես ալգորիթմ է՝ ջուր տաքացնել, թեյ դնել, սպասել։\n\n" +
      "Եթե ուզում ես, կարող ենք ալգորիթմ կազմել քո օրինակով։";
  }

  // 🤖 Ընդհանուր խելացի պատասխան
  else {
    response =
      "Ես փորձում եմ հասկանալ քո հարցը 🤔\n\n" +
      "Կփորձես այն գրել ավելի պարզ կամ օգտագործել հիմնական բառեր՞։\n\n" +
      "Օրինակ՝ թեման, դասարանը կամ կոնկրետ ինչն է դժվար։";
  }

  answerBox.innerText = response;
}
